/**
 * PrimitiveFiguresLibrary has methods to draw primitive shapes, such as a box, a cone and a V.
 * 
 * @author David Chays
 * @version 2008 Oct 3
 */
public class PrimitiveFiguresLibrary
{     
    // Draw a box.
    public static void drawBox ()
    {
        System.out.println ("+----+");
        System.out.println ("|    |");
        System.out.println ("|    |");
        System.out.println ("+----+");        
    } // end drawBox
    
    // Draw a cone.
    public static void drawCone ()
    {
        System.out.println ("  /\\ ");
        System.out.println (" /  \\");       
    } // drawCone
    
    // Draw a V.
    public static void drawV ()
    {
        System.out.println ("  /\\ ");
        System.out.println (" /  \\"); 
    } // end DrawV
    
} // end PrimitiveFiguresLibrary

